    public static Level GetLevelByCode(string code)
    {
        Regex regex = new Regex("[^a-zA-Z0-9 -]");
        Level foundLevel = null;
        foreach (Level level in levels)
        {
            if (regex.Replace(level.code, "").Equals(regex.Replace(code, "")))
                foundLevel = level;
        }

        return foundLevel;
    }

